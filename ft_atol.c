#include "push_swap.h"

long	ft_atol(const char *str)
{
	long	i;
	long	nb;
	long	signal;

	i = 0;
	nb = 0;
	signal = 1;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
	{
		signal *= -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= 48 && str[i] <= 57)
	{
		nb = (nb * 10) + (str[i]) - 48;
		i++;
	}
	return (nb * signal);
}
